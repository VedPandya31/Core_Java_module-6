package com.Module1;

public class Q6_SwitchCase
{
	public static void main(String[] args) 
	{
		int no=4;
		switch(no)
		{
		case 1:
			System.out.println("Your Item is Pizza...");
			break;
		case 2:
			System.out.println("Your Item is Burger...");
			break;
		case 3:
			System.out.println("Your Item is Dosa...");
			break;
		case 4:
			System.out.println("Invalid choice...");
			break;
		}
	}
}
